<template>
<div>
  <div id="page">

    <page-header></page-header>

    <div id="play">
      <div id="left-player">
        <player-info :player="getPlayer(0)" side="left"></player-info>
      </div>

      <div id="cards">
        <card-area></card-area>
      </div>

      <div id="right-player">
        <player-info :player="getPlayer(1)" side="right"></player-info>
      </div>
    </div>

    <div id="stacks">
     <p> This is the stacks area </p>
    </div>
    
  </div>
</div>
</template>


<script>
import PageHeader from '@/components/shared/PageHeader'
import PlayerInfo from '@/components/game/PlayerInfo'
import CardArea from '@/components/game/CardArea'
import {mapState, mapActions} from 'vuex'

export default {
  name: 'game-page',
  components: {
    'page-header': PageHeader,
    'card-area': CardArea,
    'player-info': PlayerInfo
  },
  computed: {
    ...mapState([
      'gameState',
      'players'
    ])
  },
  methods: {
    ...mapActions([
      'leaveGame'
    ]),
    getPlayer (id) {
      return this.players[id]
    }
  },
  created () {
    // If gameState is not game when we create this element (page refresh)
    // Then we want to leave the game and return to home page.
    if (this.gameState !== 'game') {
      this.leaveGame()
    }
  }
}
</script>

